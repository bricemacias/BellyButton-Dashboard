export const DomainIcons = (domain: String) => {
  switch (domain) {
    case 'Auto':
      return '🚗';
    case 'Gaming':
      return '🎮';
    case 'Food':
      return '🍔';
    case 'Lifestyle':
      return '🧘';
    case 'Humor':
      return '🤣';
    case 'History':
      return '⚔️';
    case 'Fun':
      return '🎈';
    case 'Sport':
      return '💪';
    case 'Family':
      return '👪';
  }
};
